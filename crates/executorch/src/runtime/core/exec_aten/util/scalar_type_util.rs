/// Implements type promotion rules that are consistent with ATen behaviour,
/// which in turn is consistent with NumPy's promote_types.
/// If half_to_float is set to true, then half and bfloat16 will be promoted to
/// float instead.
// [spec:et:def:scalar-type-util.executorch.runtime.promote-types]
// [spec:et:def:scalar-type-util.executorch.runtime.promote-types-fn]
// [spec:et:sem:scalar-type-util.executorch.runtime.promote-types-fn]
pub fn promote_types(a: ScalarType, b: ScalarType, half_to_float: bool) -> ScalarType {
    // For QInt types, only allow exact match
    if is_qint_type(a) && a == b {
        return a;
    }
    if is_qint_type(a) || is_qint_type(b) {
        et_check_msg!(false, "promoteTypes not valid for quantized dtypes");
    }

    // For Bits types, only allow exact match
    if is_bits_type(a) && a == b {
        return a;
    }
    if is_bits_type(a) || is_bits_type(b) {
        et_check_msg!(false, "promoteTypes not valid for bits dtypes");
    }

    // For Float8 types, only allow exact match
    if is_float8_type(a) && a == b {
        return a;
    }
    if is_float8_type(a) || is_float8_type(b) {
        et_check_msg!(false, "promoteTypes not valid for float8 dtypes");
    }

    // For barebones uint types, only allow exact match
    if is_barebones_unsigned_type(a) && a == b {
        return a;
    }
    if is_barebones_unsigned_type(a) || is_barebones_unsigned_type(b) {
        et_check_msg!(false, "promoteTypes not valid for barebone unsigned dtypes");
    }

    let ix_a = internal::DTYPE2INDEX[a as usize as usize];
    et_check!(ix_a != -1);
    let ix_b = internal::DTYPE2INDEX[b as usize as usize];
    et_check!(ix_b != -1);
    let mut promoted_type: ScalarType =
        internal::PROMOTE_TYPES_LOOKUP[ix_a as usize][ix_b as usize];

    if half_to_float && (promoted_type == ScalarType::Half || promoted_type == ScalarType::BFloat16)
    {
        promoted_type = ScalarType::Float;
    }

    promoted_type
}

//
// et_switch_*_types! macro family
//
// Literal port of the ET_SWITCH_<SET>_TYPES(dtype, ctx, name, CTYPE, body)
// family. Following PORTING.md's established shape, the Rust macro takes the
// dtype expr plus a `|CTYPE| { ... }` closure and expands to a match over the
// ScalarType arms in the set, binding a concrete `type CTYPE = <ctype>;` alias
// inside each arm before invoking the body.
//
// The `default` (unhandled dtype) arm mirrors ET_INTERNAL_SWITCH's fall-through
// (fail-fn): it records InvalidArgument on the context and logs an error, then
// yields the body's default value.
//
// [spec:et:def:scalar-type-util.executorch.runtime.fail-fn]
// [spec:et:sem:scalar-type-util.executorch.runtime.fail-fn]

/// One `case` of an internal switch: expands to a `(ScalarVariant, cpp_type)`
/// tuple of tokens consumed by `et_internal_switch!`. A `macro_rules!` invocation
/// cannot itself expand to a `match` arm (Rust forbids macros in arm position),
/// so the arm is built by `et_internal_switch!` from these token pairs instead.
#[macro_export]
macro_rules! et_internal_switch_case {
    ($scalar:ident, $cpp_type:ty) => {
        ($scalar, $cpp_type)
    };
}

/// The internal switch scaffold. `$ctx` is the KernelRuntimeContext, `$name` the
/// diagnostic op-name label, `$type` the runtime ScalarType, `$ctype_alias` the
/// per-arm concrete-type alias to bind, `$body` the shared body, and the
/// `($scalar, $cpp_type)` pairs the arms to emit. The default arm ports the
/// fail-fn behavior.
#[macro_export]
macro_rules! et_internal_switch {
    ($type:expr, $ctx:expr, $name:expr, $ctype_alias:ident, $body:expr,
        [ $( ($scalar:ident, $cpp_type:ty) ),* $(,)? ]) => {{
        let _st = $type;
        let et_switch_name = $name;
        let _ = et_switch_name;
        match _st {
            $(
                $crate::runtime::core::portable_type::scalar_type::ScalarType::$scalar => {
                    #[allow(non_camel_case_types, dead_code)]
                    type $ctype_alias = $cpp_type;
                    $body
                }
            )*
            // [spec:et:sem:scalar-type-util.executorch.runtime.fail-fn]
            _ => {
                $ctx.fail($crate::runtime::core::error::Error::InvalidArgument);
                $crate::et_log!(
                    Error,
                    "Unhandled dtype {} for {}",
                    $crate::runtime::core::exec_aten::util::scalar_type_util::to_string(_st),
                    et_switch_name
                );
                ::core::default::Default::default()
            }
        }
    }};
}

