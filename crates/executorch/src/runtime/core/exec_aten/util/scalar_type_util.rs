/// Returns true if the parameter is one of the values covered by
/// ET_FORALL_SCALAR_TYPES.
// [spec:et:def:scalar-type-util.executorch.runtime.is-valid-fn]
// [spec:et:sem:scalar-type-util.executorch.runtime.is-valid-fn]
pub fn is_valid(ty: ScalarType) -> bool {
    // PORT-NOTE: C++ compares `type < ScalarType::NumOptions` on the enum; the
    // Rust `ScalarType` does not derive `PartialOrd`, so the comparison is
    // performed on the underlying `i8` discriminants (same ordering).
    (ty as i8) >= 0 && (ty as i8) < (ScalarType::NumOptions as i8) && ty != ScalarType::Undefined
}

/// Returns the name of a ScalarType as a C string.
// [spec:et:def:scalar-type-util.executorch.runtime.to-string-fn]
// [spec:et:sem:scalar-type-util.executorch.runtime.to-string-fn]
pub fn to_string(t: ScalarType) -> &'static str {
    match t {
        ScalarType::Byte => "Byte",
        ScalarType::Char => "Char",
        ScalarType::Short => "Short",
        ScalarType::Int => "Int",
        ScalarType::Long => "Long",
        ScalarType::Half => "Half",
        ScalarType::Float => "Float",
        ScalarType::Double => "Double",
        ScalarType::ComplexHalf => "ComplexHalf",
        ScalarType::ComplexFloat => "ComplexFloat",
        ScalarType::ComplexDouble => "ComplexDouble",
        ScalarType::Bool => "Bool",
        ScalarType::QInt8 => "QInt8",
        ScalarType::QUInt8 => "QUInt8",
        ScalarType::QInt32 => "QInt32",
        ScalarType::BFloat16 => "BFloat16",
        ScalarType::QUInt4x2 => "QUInt4x2",
        ScalarType::QUInt2x4 => "QUInt2x4",
        ScalarType::Bits1x8 => "Bits1x8",
        ScalarType::Bits2x4 => "Bits2x4",
        ScalarType::Bits4x2 => "Bits4x2",
        ScalarType::Bits8 => "Bits8",
        ScalarType::Bits16 => "Bits16",
        ScalarType::Float8_e5m2 => "Float8_e5m2",
        ScalarType::Float8_e4m3fn => "Float8_e4m3fn",
        ScalarType::Float8_e5m2fnuz => "Float8_e5m2fnuz",
        ScalarType::Float8_e4m3fnuz => "Float8_e4m3fnuz",
        ScalarType::UInt16 => "UInt16",
        ScalarType::UInt32 => "UInt32",
        ScalarType::UInt64 => "UInt64",
        ScalarType::Undefined => "Undefined",
        // PORT-NOTE: The C++ `switch` has a `default` arm returning
        // "UNKNOWN_SCALAR" for any bit pattern not covered by
        // ET_FORALL_SCALAR_TYPES or Undefined. A Rust `#[repr(i8)]` enum can
        // only hold declared discriminants, so the match above is exhaustive
        // (NumOptions is the only remaining arm) and the "UNKNOWN_SCALAR"
        // fallthrough is only reachable via NumOptions.
        ScalarType::NumOptions => "UNKNOWN_SCALAR",
    }
}

/// Returns the size in bytes of the C type associated with the ScalarType.
///
/// Calls et_check_msg!() if the type is unknown or is ScalarType::Undefined.
// [spec:et:def:scalar-type-util.executorch.runtime.element-size-fn]
// [spec:et:sem:scalar-type-util.executorch.runtime.element-size-fn]
pub fn element_size(t: ScalarType) -> usize {
    match t {
        ScalarType::Byte => core::mem::size_of::<u8>(),
        ScalarType::Char => core::mem::size_of::<i8>(),
        ScalarType::Short => core::mem::size_of::<i16>(),
        ScalarType::Int => core::mem::size_of::<i32>(),
        ScalarType::Long => core::mem::size_of::<i64>(),
        ScalarType::Half => core::mem::size_of::<Half>(),
        ScalarType::Float => core::mem::size_of::<f32>(),
        ScalarType::Double => core::mem::size_of::<f64>(),
        ScalarType::ComplexHalf => core::mem::size_of::<ComplexHalf>(),
        ScalarType::ComplexFloat => core::mem::size_of::<ComplexFloat>(),
        ScalarType::ComplexDouble => core::mem::size_of::<ComplexDouble>(),
        ScalarType::Bool => core::mem::size_of::<bool>(),
        ScalarType::QInt8 => core::mem::size_of::<qint8>(),
        ScalarType::QUInt8 => core::mem::size_of::<quint8>(),
        ScalarType::QInt32 => core::mem::size_of::<qint32>(),
        ScalarType::BFloat16 => core::mem::size_of::<BFloat16>(),
        ScalarType::QUInt4x2 => core::mem::size_of::<quint4x2>(),
        ScalarType::QUInt2x4 => core::mem::size_of::<quint2x4>(),
        ScalarType::Bits1x8 => core::mem::size_of::<bits1x8>(),
        ScalarType::Bits2x4 => core::mem::size_of::<bits2x4>(),
        ScalarType::Bits4x2 => core::mem::size_of::<bits4x2>(),
        ScalarType::Bits8 => core::mem::size_of::<bits8>(),
        ScalarType::Bits16 => core::mem::size_of::<bits16>(),
        ScalarType::Float8_e5m2 => core::mem::size_of::<unused_dtype::Float8_e5m2>(),
        ScalarType::Float8_e4m3fn => core::mem::size_of::<unused_dtype::Float8_e4m3fn>(),
        ScalarType::Float8_e5m2fnuz => core::mem::size_of::<unused_dtype::Float8_e5m2fnuz>(),
        ScalarType::Float8_e4m3fnuz => core::mem::size_of::<unused_dtype::Float8_e4m3fnuz>(),
        ScalarType::UInt16 => core::mem::size_of::<u16>(),
        ScalarType::UInt32 => core::mem::size_of::<u32>(),
        ScalarType::UInt64 => core::mem::size_of::<u64>(),
        _ => {
            et_check_msg!(false, "Unknown ScalarType {}", t as i8);
            // et_check_msg! aborts; the C++ switch has no return here.
            unreachable!()
        }
    }
}

