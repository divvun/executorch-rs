    pub const fn calculate_dtype2index() -> [i64; ScalarType::NumOptions as usize] {
        let mut inverse: [i64; ScalarType::NumOptions as usize] =
            [0; ScalarType::NumOptions as usize];
        let mut i: i64 = 0;
        while i < ScalarType::NumOptions as i64 {
            inverse[i as usize] = -1;
            i += 1;
        }
        let mut i: i64 = 0;
        while i < INDEX2DTYPE.len() as i64 {
            inverse[INDEX2DTYPE[i as usize] as usize] = i;
            i += 1;
        }
        inverse
    }

