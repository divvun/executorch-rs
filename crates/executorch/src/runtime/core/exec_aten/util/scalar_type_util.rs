// [spec:et:def:scalar-type-util.executorch.runtime.to-complex-type-fn]
// [spec:et:sem:scalar-type-util.executorch.runtime.to-complex-type-fn]
pub fn to_complex_type(t: ScalarType) -> ScalarType {
    match t {
        ScalarType::BFloat16 => {
            // BFloat16 has range equivalent to Float,
            // so we map it to ComplexFloat.
            ScalarType::ComplexFloat
        }
        ScalarType::Half => ScalarType::ComplexHalf,
        ScalarType::Float => ScalarType::ComplexFloat,
        ScalarType::Double => ScalarType::ComplexDouble,
        ScalarType::ComplexHalf => ScalarType::ComplexHalf,
        ScalarType::ComplexFloat => ScalarType::ComplexFloat,
        ScalarType::ComplexDouble => ScalarType::ComplexDouble,
        _ => {
            et_check_msg!(false, "Unknown Complex ScalarType for {}", t as i8);
            unreachable!()
        }
    }
}

/// Encodes type casting rules that are consistent with ATen behaviour.
// [spec:et:def:scalar-type-util.executorch.runtime.can-cast-fn]
// [spec:et:sem:scalar-type-util.executorch.runtime.can-cast-fn]
pub const fn can_cast(from: ScalarType, to: ScalarType) -> bool {
    // Disallow complex -> non-complex
    !(is_complex_type(from) && !is_complex_type(to)) &&
        // Disallow float -> integral
        !(is_floating_type(from) && is_integral_type(to, /*includeBool=*/ false)) &&
        // Treat bool as a special category. Disallow non-bool -> bool
        !(!matches!(from, ScalarType::Bool) && matches!(to, ScalarType::Bool))
}

/// When casting from floating point to integral type, if the floating value is
/// outside the integral type range, then an error is thrown if sanitization is
/// enabled. To circumvent this, we cast the floating point to i64 first.
///
/// PORT-NOTE: The C++ `convert<To, From>` is a compile-time SFINAE-dispatched
/// template with two overloads (float->integral, and everything else). Rust has
/// no direct SFINAE analogue; the caller selects the right conversion path.
/// `convert_float_to_int` mirrors the float->integral overload
/// (`static_cast<To>(static_cast<int64_t>(val))`) and `convert` mirrors the
/// plain `static_cast<To>(val)` overload via the `From: Into<To>` bound.
// [spec:et:def:scalar-type-util.executorch.runtime.convert-fn]
// [spec:et:sem:scalar-type-util.executorch.runtime.convert-fn]
pub fn convert<To, From>(val: From) -> To
where
    From: Into<To>,
{
    val.into()
}

