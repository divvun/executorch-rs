// [spec:et:sem:scalar-type-util.executorch.runtime.is-integral-type-fn]
pub const fn is_integral_type(t: ScalarType, include_bool: bool) -> bool {
    (include_bool && matches!(t, ScalarType::Bool))
        || matches!(
            t,
            ScalarType::Byte
                | ScalarType::Char
                | ScalarType::Int
                | ScalarType::Long
                | ScalarType::Short
        )
}

