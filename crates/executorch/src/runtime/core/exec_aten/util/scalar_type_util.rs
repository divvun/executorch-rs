// PORT-NOTE: `ET_CHECK`/`ET_CHECK_MSG` live in runtime/platform/assert.h, which
// has no ported `assert.rs` target yet. These local macros mirror the semantics
// (emit message, then abort via the PAL abort path), matching the pattern used
// in runtime/core/portable_type/scalar.rs. Should be replaced by the shared
// `et_check_msg!`/`et_check!` once the assert module is ported. Unresolved
// cross-module reference.
macro_rules! et_check_msg {
    ($cond:expr, $($arg:tt)*) => {
        if !($cond) {
            crate::runtime::platform::abort::runtime_abort();
        }
    };
}

macro_rules! et_check {
    ($cond:expr) => {
        if !($cond) {
            crate::runtime::platform::abort::runtime_abort();
        }
    };
}

