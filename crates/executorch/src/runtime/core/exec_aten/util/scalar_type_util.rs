    // The internal-switch default (fail-fn) arm: an unhandled dtype records
    // InvalidArgument on the context and yields the body's Default value.
    // [spec:et:sem:scalar-type-util.executorch.runtime.fail-fn/test]
    #[test]
    fn scalar_type_util_test_switch_fail_fn() {
        use crate::runtime::core::error::Error;
        use crate::runtime::core::memory_allocator::{MemoryAllocator, MemoryAllocatorBase};
        use crate::runtime::kernel::kernel_runtime_context::KernelRuntimeContext;

        // The fail-fn default arm calls et_log!, which requires the PAL to be
        // initialized (mirrors the C++ fixtures' runtime_init()).
        crate::runtime::platform::runtime::runtime_init();

        let null_tracer = crate::extension::module::module::null_event_tracer();
        let null_alloc = core::ptr::null_mut::<MemoryAllocator>() as *mut dyn MemoryAllocatorBase;

        let mut ctx = KernelRuntimeContext::new(null_tracer, null_alloc);
        // Bool is not in ET_SWITCH_REAL_TYPES, so it hits the fail-fn default arm.
        let out: i32 =
            crate::et_switch_real_types!(ScalarType::Bool, ctx, "switch_fail_fn_test", CTYPE, {
                core::mem::size_of::<CTYPE>() as i32
            });
        assert_eq!(out, i32::default());
        assert_eq!(ctx.failure_state(), Error::InvalidArgument);

        // A handled dtype runs the body and leaves the context Ok.
        let mut ctx_ok = KernelRuntimeContext::new(null_tracer, null_alloc);
        let sz: i32 =
            crate::et_switch_real_types!(ScalarType::Int, ctx_ok, "switch_fail_fn_test", CTYPE, {
                core::mem::size_of::<CTYPE>() as i32
            });
        assert_eq!(sz, 4);
        assert_eq!(ctx_ok.failure_state(), Error::Ok);
    }

